  <h1>Introducing Arctic</h1>
  <p>Arctic is a design system wrapped in a single library with powerful utilities that will allow anyone to build digital products.</p>
  <p>While being humans interacting with machine interfaces, we need to solve accessibility as well as usability.</p>
  <p>Just installing Artic to your project will solve most of these issues.</p>
  <p>To give developers full transparency there are full HTML and CSS reference documents for your convenience.</p>
