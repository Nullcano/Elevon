  <h1>Plug-and-Play</h1>

  <p>Elevon is an extendable design system for web-applications that favors accessible user interfaces
    with a modern take on design and experience built-in to the semantic HTML elements.</p>

  <p>While providing an opinionated style by default, Elevon includes a rich library of classes and shorthands
    to let designers apply our classes to any element. Developers may extend our library
    and add their own classes alongside Elevon.</p>

  <p>Our mission is to craft smarter semantic HTML elements that just works out of the box
    and focus on creating a powerful shorthand-library that will accelerate developers workflow
    when creating layouts and styling elements.</p>

  <p>To give developers full transparency <a href="html">there are HTML</a> and <a href="css">CSS reference documents</a> with previews for your convenience.</p>
